const convert = function(str, find, replace, chance) {
  const arr = str.split(find);
  const tail = arr.pop();
  const result = [];
  for (let item of arr) {
    result.push(item, Math.random() < chance ? replace : find);
  }
  result.push(tail);
  return result.join('');
}
const c01 = function(str) {
  str = convert(str, '0', '1', 0.08);
  str = convert(str, '1', '0', 0.08);
  return str;
}

